package cap_03;

public class Resuelto01_Multi {
	
	public static void main(String[] args) {
		
		for (int i = 5; i < 15; i++) {
			System.out.println(i);
		}
		
		System.out.println("--------------------------");

		int num = 5;
		while (num < 15) {
			System.out.println(num);
			num++;
		}
		
	}

}
