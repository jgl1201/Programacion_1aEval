package cap_03;

public class Propuesto11a_50Pares {
	
	public static void main(String[] args) {
		
		for(int i = 2; i <= 100 ; i += 2) {
			System.out.println(i);
		}
		System.out.println("-------------------");
		
		for(int j = 1; j <= 50; j++) {
			System.out.println(j*2);
		}
	}

}
