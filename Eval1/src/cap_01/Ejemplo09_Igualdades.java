package cap_01;

public class Ejemplo09_Igualdades {

	public static void main(String[] args) {

		int m = 2, n = 5;
		boolean res;
		res = m > n;// res=false
		System.out.println("res es:" + res);
		res = m < n;// res=true
		System.out.println("res es:" + res);
		res = m >= n;// res=false
		System.out.println("res es:" + res);
		res = m <= n;// res=true
		System.out.println("res es:" + res);
		res = m == n;// res=false
		System.out.println("res es:" + res);
		res = m != n;// res=true
		System.out.println("res es:" + res);

	}

}
