package cap_01;

public class Ejemplo12_ExpsLogicas {

	public static void main(String[] args) {

		int a = 5, b = 2;
		a += b;
		System.out.println("a vale ahora :" + a);
		a -= b;
		System.out.println("a vale ahora :" + a);
		a *= b;
		System.out.println("a vale ahora :" + a);
		a /= b;
		System.out.println("a vale ahora :" + a);
		a %= b;
		System.out.println("a vale ahora :" + a);

	}

}
